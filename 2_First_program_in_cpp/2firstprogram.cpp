// Data types in cpp 

#include <iostream>
using namespace std ;

int main(){
    // integer 
    int a = 78 ;
    cout << a << endl;
    
    // float 
    float b = 1.2 ;
    cout << b << endl;

    //  double 
    double d = 2.34 ;
    cout << d << endl;

    // char 
    char ch = 'a';
    cout << ch << endl;

    // boolean 
    bool i = true ;
    bool j = false ;

    cout << i << endl ;
    cout << j << endl;
    
    // sizeof Operator 
     int size = sizeof(a);
     cout << size << endl;

    //  Typecasting 
     char f = 99;
     cout << f << endl;

    //  other examples 
    cout << 12.54 / 7 << endl;

    // unsigned operator 
    unsigned int o = 67 ;
    cout  << o << endl;

    // storing negative numbers 
    int e = -23;
    cout << e << endl;
    return 0 ;
}

